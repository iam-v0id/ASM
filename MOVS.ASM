ASSUME CS:CODE,DS:DATA,ES:EXTRA
DATA SEGMENT
    SRCDATA DB "MPMC LAB $"
    COUNT EQU ($-SRCDATA)
DATA ENDS

EXTRA SEGMENT
    DSTDATA DB 12 DUP (0)
EXTRA ENDS

CODE SEGMENT
    START:
        MOV AX,DATA
        MOV DS,AX
        MOV AX,EXTRA
        MOV ES,AX
        mov CX,COUNT
        MOV SI,OFFSET SRCDATA
        MOV DI,OFFSET DSTDATA
        CLD
        REP MOVSB
        INT 03H
CODE ENDS
    END START
